class QtDataView(QtGui.QTreeView):

    def __init__(self, model=None, parent=None):
        super(QtDataView, self).__init__(parent)

        self.setModel(model)

        self.connect(self,
                     QtCore.SIGNAL('activated(QModelIndex)'),
                     model.itemActivated)
